#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char	*original_dest;

	original_dest = dest;
	while (*src && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (original_dest);
}
